'''Disini kita menguji apakah sebuah bilangan adalah bilangan positif , nol, atau negatif dan menampilkan hasilnya ke layar'''

bilangan = 5.5
'''Coba juga mengganti bilangan jadi 
   bilangan = 0
   bilangan = -5.5 '''

if bilangan > 0:
    print("Bilangan positif")
elif bilangan == 0:
    print("Nol")
else:
    print("Bilangan negatif")
